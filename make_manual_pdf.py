"""차병원 인턴 턴표 교환 시스템 - 사용 설명서 PDF 생성"""
from fpdf import FPDF
import os

FONT_R  = r'C:\Windows\Fonts\malgun.ttf'
FONT_B  = r'C:\Windows\Fonts\malgunbd.ttf'
OUTPUT  = os.path.join(os.path.dirname(os.path.abspath(__file__)), '사용설명서.pdf')

# ── 색상 팔레트 ──────────────────────────────────────────────────────────────
BLUE_DARK   = (31,  97, 141)
BLUE_MID    = (52, 152, 219)
BLUE_LIGHT  = (214, 234, 248)
GRAY_LIGHT  = (245, 245, 245)
YELLOW_LIGHT= (254, 249, 231)
GREEN_LIGHT = (232, 245, 233)
WHITE       = (255, 255, 255)
BLACK       = (30,  30,  30)
GRAY_TEXT   = (100, 100, 100)

class ManualPDF(FPDF):
    def __init__(self):
        super().__init__(orientation='P', unit='mm', format='A4')
        self.add_font('mg',  '',  FONT_R)
        self.add_font('mg',  'B', FONT_B)
        self.set_auto_page_break(auto=True, margin=18)
        self.set_margins(18, 18, 18)

    # ── 헤더 ─────────────────────────────────────────────────────────────────
    def header(self):
        self.set_fill_color(*BLUE_DARK)
        self.set_text_color(*WHITE)
        self.set_font('mg', 'B', 12)
        self.cell(0, 11, '  차병원 인턴 턴표 교환 시스템  |  사용 설명서', fill=True, ln=True)
        self.set_text_color(*BLACK)
        self.ln(3)

    # ── 푸터 ─────────────────────────────────────────────────────────────────
    def footer(self):
        self.set_y(-13)
        self.set_font('mg', '', 8)
        self.set_text_color(*GRAY_TEXT)
        self.cell(0, 6, f'- {self.page_no()} -', align='C')
        self.set_text_color(*BLACK)

    # ── 섹션 제목 ────────────────────────────────────────────────────────────
    def section(self, title):
        self.ln(4)
        self.set_fill_color(*BLUE_MID)
        self.set_text_color(*WHITE)
        self.set_font('mg', 'B', 12)
        self.cell(0, 9, f'  {title}', fill=True, ln=True)
        self.set_text_color(*BLACK)
        self.ln(2)

    # ── 소제목 ───────────────────────────────────────────────────────────────
    def sub(self, title):
        self.ln(2)
        self.set_font('mg', 'B', 10)
        self.set_text_color(*BLUE_DARK)
        self.cell(0, 7, f'  {title}', ln=True)
        self.set_text_color(*BLACK)

    # ── 본문 ─────────────────────────────────────────────────────────────────
    def body(self, text):
        self.set_font('mg', '', 10)
        self.set_text_color(*BLACK)
        self.multi_cell(0, 6, text)
        self.ln(1)

    # ── 안내 박스 ────────────────────────────────────────────────────────────
    def box(self, text, bg=BLUE_LIGHT):
        self.set_fill_color(*bg)
        self.set_font('mg', '', 10)
        self.multi_cell(0, 6, f'  {text}', fill=True)
        self.ln(2)

    # ── 테이블 행 ────────────────────────────────────────────────────────────
    def trow(self, c1, c2, c1w=45, header=False):
        h = 7
        if header:
            self.set_fill_color(*BLUE_DARK)
            self.set_text_color(*WHITE)
            self.set_font('mg', 'B', 10)
        else:
            self.set_fill_color(*GRAY_LIGHT)
            self.set_text_color(*BLACK)
            self.set_font('mg', '', 10)
        self.cell(c1w, h, f' {c1}', border=1, fill=True)
        self.cell(0,   h, f' {c2}', border=1, fill=True, ln=True)
        self.set_text_color(*BLACK)

    # ── 글머리 항목 ──────────────────────────────────────────────────────────
    def item(self, text, indent=5):
        self.set_font('mg', '', 10)
        x = self.get_x()
        self.cell(indent)
        self.multi_cell(0, 6, f'- {text}')
        self.ln(0.5)


